""" Dise√±a un programa que, a partir del valor de los dos lados de un rectangulo (4 y 6 metros, respectivamente), muestre
el valor de su perimetro (en metros) y el de su area (en metros cuadrados).
(El perimetro debe darte 20 metros y el area 24 metros cuadrados.) """

a = 4
b = 6

# Area (A) rectangulo
A = a * b

# Perimetro (P) rectangulo

P = (2*a) + (2*b)

print('Area del rectangulo: ', A, ' m^2')
print('Perimetro del rectangulo: ', P, ' m')